package com.javarush.task.task04.task0433;

/* 
Гадание на долларовый счет
*/

public class Solution {
    public static void main(String[] args) {
        int i = 10;
        while ( i >=1 ){
            int a = 10;
            while ( a >= 1){
                System.out.print("S");
                a--;
            }
            System.out.println();
            i--;
        }

    }
}
